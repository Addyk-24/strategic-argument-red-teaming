theme = gr.themes.Default(neutral_hue="slate").set(
    body_background_fill="#111827", body_text_color="#f3f4f6",
    block_background_fill="#1f2937", block_border_color="#374151"
)

with gr.Blocks(theme=theme, css=".gradio-container {max-width: 1000px !important;}") as demo:
    
    # Hidden States
    timeline_state = gr.State(value=[])
    total_reward_state = gr.State(value=0.0)
    current_phase_state = gr.State(value="STANDBY")
    
    # 1. Header & Metric Cards
    gr.Markdown("## 🛡️ Strategic Argument Command Center")
    with gr.Row():
        last_step_card = gr.HTML(render_metric_card("LAST STEP REWARD", "--", "#3b82f6"))
        total_rew_card = gr.HTML(render_metric_card("TOTAL REWARD", "--", "#f97316"))
        score_card = gr.HTML(render_metric_card("SCORE", "--", "#22c55e"))
        status_card = gr.HTML(render_metric_card("STATUS", "STANDBY", "#6b7280"))

    # 2. Current State (Replicates the "Current Ticket" box)
    gr.Markdown("### Current Debate State")
    with gr.Group():
        pills_html = gr.HTML(render_pills("Medium", "STANDBY"))
        gr.Markdown("**LATEST OPPONENT CHALLENGE**")
        opponent_text_output = gr.HTML("<div style='padding: 15px; background: #111827; border-radius: 6px; border: 1px solid #374151; color: #d1d5db;'>Initialize environment to begin...</div>")

    # 3. Instruction Banner
    gr.HTML("""
        <div style="background-color: rgba(34, 197, 94, 0.1); border: 1px solid #22c55e; border-radius: 8px; padding: 12px; color: #d1d5db; margin-top: 15px; margin-bottom: 15px;">
            <strong>Environment Rules:</strong> Follow the strict progression: classify → route → respond. Utilize the auto-fill functionality to automatically populate the optimal evaluation payload for the current phase.
        </div>
    """)

    # 4. Step-by-step Action Area
    gr.Markdown("### Step-by-step action")
    with gr.Group():
        with gr.Row():
            topic_input = gr.Textbox(label="Debate Topic", value="Universal Basic Income is necessary", scale=3)
            difficulty_input = gr.Dropdown(label="Difficulty", choices=["Easy", "Medium", "Hard"], value="Medium", scale=1)
            
        phase_tag_input = gr.Dropdown(label="Action Type (Phase Tag)", choices=["OPENING", "CHALLENGE", "REBUTTAL", "CONSOLIDATION", "CLOSING"], value="OPENING")
        argument_input = gr.Textbox(label="Argument Payload (matches POST /step JSON)", lines=4)
        
        with gr.Row():
            execute_btn = gr.Button("EXECUTE STEP", variant="primary", scale=2)
            reset_btn = gr.Button("RESET ENVIRONMENT", scale=1)
            autofill_btn = gr.Button("⚡ AUTO-FILL IDEAL MOVE", scale=2)

    # 5. Custom Action Timeline
    gr.Markdown("### Action Timeline")
    timeline_output = gr.HTML(render_timeline([]))

    # --- Event Listeners ---
    
    # Auto-Fill Logic
    autofill_btn.click(
        fn=auto_fill_action,
        inputs=[current_phase_state],
        outputs=[phase_tag_input, argument_input]
    )

    # Reset Logic
    reset_btn.click(
        fn=reset_env_ui, 
        inputs=[topic_input, difficulty_input], 
        outputs=[last_step_card, total_rew_card, score_card, status_card, pills_html, opponent_text_output, timeline_output, timeline_state, total_reward_state, current_phase_state]
    )
    
    # Step/Execute Logic
    execute_btn.click(
        fn=step_env_ui, 
        inputs=[argument_input, phase_tag_input, timeline_state, total_reward_state, difficulty_input], 
        outputs=[last_step_card, total_rew_card, score_card, status_card, pills_html, opponent_text_output, timeline_output, timeline_state, total_reward_state, current_phase_state]
    )

# Mount on FastAPI
app = gr.mount_gradio_app(app, demo, path="/")

